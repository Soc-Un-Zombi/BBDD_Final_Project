    private void afegir(){
        String a = "";
        
        if (pare_v == null){
            a = "'" + propietari_combobox.getSelectedItem() + "'";
        }
        else{
            StringTokenizer st = new StringTokenizer(propietari_combobox.getSelectedItem().toString(),",");
            a = st.nextToken();
        }
        
        
        String sql = "INSERT INTO bikes (licenseplate, brand, model, motor, memberid) "
        
                + "VALUES (" + 
                
                "'" + matricula_textfield.getText() + "'"
                
                + "," +
                
                "'" + marcal_textfield.getText() + "'"
                
                + "," +
                
                "'" + model_textfield.getText() + "'"
                
                + "," +
                
                "'" + cilindrada_textfield.getText()+" ("+caballs_textfield.getText() + ")'"
                
                + "," +
                                
                a+
                
                ");";
        
        System.out.println(sql);
        try {
            
            stmt = con.createStatement();          
        
            stmt.executeUpdate(sql);
            
            JOptionPane.showMessageDialog(this, "S'ha afegit correctament!", "Status", JOptionPane.INFORMATION_MESSAGE);
            
            
        } catch (SQLException e){
            
            JOptionPane.showMessageDialog(this, e.getMessage(), "Status", JOptionPane.INFORMATION_MESSAGE);           
            
        }
    }
    
    private void carrega_propietari(){
        ResultSet res = executeQuery(con, "SELECT memberid, firstname, lastname FROM members;");
        loadBranches(res,propietari_combobox);
    }
    
    private ResultSet executeQuery (Connection con, String query){
        
        try {
        
            stmt = con.createStatement();
            
            rs = stmt.executeQuery(query);           
            
            return rs;
        
        } catch (SQLException e){
          
            //JOptionPane.showMessageDialog(this, e.getMessage(), "Status", JOptionPane.INFORMATION_MESSAGE);
            
            return null;
        }
    }
    
    private void loadBranches(ResultSet rs_o, javax.swing.JComboBox<String> in ){       
        in.removeAllItems();
        
        try {
            ResultSetMetaData rsmd = rs_o.getMetaData();
            int columnas = rsmd.getColumnCount();
            while ( rs_o.next() ) {  
                String result = "";
                for (int i = 1; i < columnas; i++){
                    result = result+rs_o.getString(i)+", ";
                }
                result = result+rs_o.getString(columnas);
               in.addItem(result);                        
               
            }  
        } catch (SQLException e){
            
             //JOptionPane.showMessageDialog(this, e.getMessage(), "Status", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
    private void comprovar(){
        affegir_button.setEnabled(revisar());
    }
    
    private boolean revisar(){
        if (matricula_textfield.getText().isBlank()){
            return false;
        }
        if (marcal_textfield.getText().isBlank()){
            return false;
        }
        if (model_textfield.getText().isBlank()){
            return false;
        }
        
        if (cilindrada_textfield.getText().isBlank()){
            return false;
        }
        if (!cilindrada_textfield.getText().isBlank()){
            try {
                int numero = Integer.parseInt(cilindrada_textfield.getText());
                
                if (1 > numero){
                    return false;
                }
            } catch (NumberFormatException e){
                return false;
            }
        }
        
        if (caballs_textfield.getText().isBlank()){
            return false;
        }
        if (!caballs_textfield.getText().isBlank()){
            try {
                int numero = Integer.parseInt(caballs_textfield.getText());
                
                if (1 > numero){
                    return false;
                }
            } catch (NumberFormatException e){
                return false;
            }
        }
        return true;
        
    }
