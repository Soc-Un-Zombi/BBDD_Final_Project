    private void busqueda(String in){
        String ti = "";
        if (jComboBox1.getSelectedItem().equals("Tots")){
            ti = "";
        }
        else{
            ti = jComboBox1.getSelectedItem().toString();
        }
        
        String a = "SELECT * from elimina_arma WHERE (brand LIKE '%"+in+"%' OR model LIKE '%"+in+"%'"+ 
                ") AND guntype LIKE '%"+ti+"%'"+
                ";";
        ResultSet result = executeQuery(con, a);            
        loadBranches(result);
    }
    
    public void cerca_base_dades(){
        
        ResultSet result = executeQuery(con, "SELECT * from elimina_arma;");            
        loadBranches(result);
        
    }
    
    private void loadBranches(ResultSet branches){
        
        DefaultListModel model = new DefaultListModel();
        
        model.clear();
        
        try {
            
            while ( rs.next() ) {            
            
                String license = rs.getString(1);
                String brand = rs.getString(2);
                String model_b = rs.getString(3);
                String motor = rs.getString(4);
                               
                String result = license + ". " +brand + " " + model_b + " \"" + motor+"\"";
                model.addElement(result);                        
               
            }
            
        } catch (SQLException e){
            
             //JOptionPane.showMessageDialog(this, e.getMessage(), "Status", JOptionPane.INFORMATION_MESSAGE);
        }
        
        this.jList1.setModel(model);
    }
    
    private ResultSet executeQuery (Connection con, String query){
        
        try {
        
            stmt = con.createStatement();
            
            rs = stmt.executeQuery(query);           
            
            return rs;
        
        } catch (SQLException e){
          
            //JOptionPane.showMessageDialog(this, e.getMessage(), "Status", JOptionPane.INFORMATION_MESSAGE);
            
            return null;
        }
    }
    private void assignar_tipus(){
        ResultSet result = executeQuery(con, "SELECT guntype from elimina_arma GROUP BY guntype;");            
        updateBranches(result, jComboBox1);
    }
    
