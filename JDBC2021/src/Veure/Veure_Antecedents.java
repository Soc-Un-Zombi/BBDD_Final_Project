    private void busqueda(){
        String a;
        if (nom_combo.getSelectedItem().equals("Tots") && delicte_combo.getSelectedItem().equals("Tots")){
            a = "SELECT * from veure_antecedents;";
        }
        else if (!nom_combo.getSelectedItem().equals("Tots") && delicte_combo.getSelectedItem().equals("Tots")){
            StringTokenizer st = new StringTokenizer(nom_combo.getSelectedItem().toString(),",");
            String no = st.nextToken();
            String co = st.nextToken();
            co = co.substring(1);
            a = "SELECT * from veure_antecedents WHERE firstname ='"+no+"' AND lastname ='"+co+"';";
        }
        else if (nom_combo.getSelectedItem().equals("Tots") && !delicte_combo.getSelectedItem().equals("Tots")){
            a = "SELECT * from veure_antecedents WHERE crime ='"+delicte_combo.getSelectedItem()+"';";
        }
        else{
            StringTokenizer st = new StringTokenizer(nom_combo.getSelectedItem().toString(),",");
            String no = st.nextToken();
            String co = st.nextToken();
            a = "SELECT * from veure_antecedents WHERE (firstname ='"+no+"' AND lastname ='"+co+"') AND crime='"+delicte_combo.getSelectedItem()+";";
        }
        
        System.out.println(a);
        ResultSet result = executeQuery(con, a);            
        loadBranches(result);
    }
    
    public void cerca_base_dades(){
        ResultSet result = executeQuery(con, "SELECT * from veure_antecedents;");            
        loadBranches(result);
        update_delicte();
        
    }
    
    private void loadBranches(ResultSet branches){
        
        DefaultListModel model = new DefaultListModel();
        
        model.clear();
        
        try {
            while (branches.next()) {            
            
                String license = rs.getString(1);
                String brand = rs.getString(2);
                String model_b = rs.getString(3);
                String motor = rs.getString(4);
                String motor2 = rs.getString(5);               
                String result = license + " " +brand + "," + model_b + "," + motor+","+ motor2;
                model.addElement(result);                       
               
                                         
               
            }
            
        } catch (SQLException e){
            
             //JOptionPane.showMessageDialog(this, e.getMessage(), "Status", JOptionPane.INFORMATION_MESSAGE);
        }
        
        this.jList1.setModel(model);
    }
    
    private ResultSet executeQuery (Connection con, String query){
        
        try {
        
            stmt = con.createStatement();
            
            rs = stmt.executeQuery(query);           
            
            return rs;
        
        } catch (SQLException e){
          
            //JOptionPane.showMessageDialog(this, e.getMessage(), "Status", JOptionPane.INFORMATION_MESSAGE);
            
            return null;
        }
    }
    
    private void update_nom(){
        ResultSet res = executeQuery(con, "SELECT firstname, lastname FROM members;");
        updateBranches(res,nom_combo);
    }
    
    private void update_delicte(){
        ResultSet res = executeQuery(con, "SELECT DISTINCT crime FROM criminalrecord;");
        updateBranches(res,delicte_combo);
    }
    
    private void updateBranches(ResultSet rs_o, javax.swing.JComboBox<String> in ){       
        try {
            ResultSetMetaData rsmd = rs_o.getMetaData();
            int columnas = rsmd.getColumnCount();
            while ( rs_o.next() ) {  
                String result = "";
                for (int i = 1; i < columnas; i++){
                    result = result+rs_o.getString(i)+", ";
                }
                result = result+rs_o.getString(columnas);
               in.addItem(result);                        
               
            }  
        } catch (SQLException e){
            
             //JOptionPane.showMessageDialog(this, e.getMessage(), "Status", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
    
