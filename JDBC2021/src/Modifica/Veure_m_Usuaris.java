    private void busqueda(String in){
        String a = "SELECT * from members WHERE firstname LIKE '%"+in+"%' OR lastname LIKE '%"+in+"%'"+ 
                "OR nickname LIKE '%"+in+"%'"+
                "OR headquarter LIKE '%"+in+"%'"+
                ";";
        ResultSet result = executeQuery(con, a);            
        loadBranches(result);
    }
    
